a = 10
b = 5

#arithmetic operator
print(a + b);
print(a - b);
print(a * b);
print(a / b);
print(a % b);
print(a**b);

#Relational / Comparison Operator

"""
> , >= , < , <= , == ,!=
"""
print(a < b)
print(a <= b)
print(a > b)
print(a >= b)
print(a== b)
print(a!= b)

#Assignment Operator
"""
=, +=, -=, *= , /=
"""
a /= b
print(a)

#logical opertor

"""
not and or
"""

print(not (a < b))
print((a > b) and (a < b))
print((a > b) or (a < b))